var sumar = function (num1, num2) {
  return num1 + num2;
};

var multiplicar = function (num1, num2) {
  return num1 * num2;
};

var restar = function (num1, num2) {
  return num1 - num2;
};

var division = function (num1, num2) {
  return num1 / num2;
};

export { sumar, multiplicar, restar, division };
